/**
 * add - fun
 *
 * @b: var
 * @a: var
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - fun
 *
 * @a: var
 * @b: var
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - fun
 *
 * @a: var
 * @b: var
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - fun
 *
 * @a: var
 * @b: var
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - fun
 *
 * @a: var
 * @b: var
 * Return: int
 */

int mod(int a, int b)
{
	return (a % b);
}
